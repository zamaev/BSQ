char	*ft_strcapitalize(char *str)
{
	int lc;

	lc = '+';
	while (*str)
	{
		if ((lc < 'a' || lc > 'z') && !(lc > '0' && lc < '9'))
		{
			lc = *str;
			if (*str >= 'a' && *str <= 'z')
			{
				*str -= 32;
			}
		}
		else
		{
			lc = *str;
		}
		str++;
	}
	return (str);
}
