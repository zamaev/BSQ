#include <unistd.h>

void	print_str(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int		main(int argc, char **argv)
{
	while (argc > 1)
	{
		print_str(argv[argc - 1]);
		argc--;
	}
	return (0);
}
