#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;
	int		n;

	if (nb < 0)
	{
		write(1, "-", 1);
	}
	c = nb > 0 ? '0' + nb % 10 : '0' - nb % 10;
	n = nb > 0 ? nb / 10 : nb / 10 * -1;
	if (n != 0)
	{
		ft_putnbr(n);
	}
	write(1, &c, 1);
}
